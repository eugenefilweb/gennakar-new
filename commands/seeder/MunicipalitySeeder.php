	public function attributes($key)
	{
        list($name, $region_no, $province_no, $no) = self::DATA[$key - 1];

        $province = Province::find()
            ->alias('p')
            ->joinWith(['region r'])
            ->where([
                'r.no' => $region_no,
                'p.no' => $province_no
            ])
            ->groupBy('p.id')
            ->one();

        return [
            'name' => $name,
            'province_id' => ($province)? $province->id: 0,
            'no' => $no,
            'created_at' => new Expression('UTC_TIMESTAMP'),
            'updated_at' => new Expression('UTC_TIMESTAMP'),
        ];
	}
