<div class="text-center py-15 pt-30">
	<h1 class="display-3 font-weight-bolder">DATA PRIVACY CONSENT / AGREEMENT </h1>
</div>
<p class="indent pb-2">
	The Municipal Government of Gen. Nakar, Quezon Province recognize their responsibilities under the Republic Act No. 10173 (Act), also known as the Data Privacy Act of 2012, with respect to the data they collect, record, organize, update, use, consolidate or destruct from the citizens and public service clients. The personal data obtained from this portal/form is entered and stored within the LGU's authorized information and communications system and IT-service provider and will only be accessed by LGU authorized personnel. The Municipal Government have instituted appropriate organizational, technical and physical security measures to ensure the protection of the citizens and public service client’s personal data.
</p>

<p class="mb-0">
	Furthermore, the information collected and stored in the portal/form shall only be used for the following purposes:
</p>
<ul class="pb-2 pl-25">
	<li>Processing and reporting of documents related to the accident/event/activity/public service provision.</li>
	<li>Request of authorized National Government Agencies with due jurisdiction over the accident/event/activity/public service provision</li>
	<li>Valid request of duly authorized insurance company/provider by the parties involved in an accident or event that will trigger an insurance claim or coverage and participation from the insurance company to process claims of parties involved provided that such request has undergone the proper request procedure of the assigned LGU office </li>
	<li>Activities pertaining to local development planning and organizational goal setting, performance reviews and process improvements.</li> 
</ul>
<p class="indent pb-2">
	The Municipal Government and its IT-service provider shall not disclose the personal information of citizens or public service client outside of the aforementioned use of data without their consent and shall retain this information over a period of ten years for the effective implementation and management of public services. I have read the Municipal Government’s Data Privacy Statement and express my consent for the Municipal Government and its IT-service provider to collect, record, organize, update, or modify, retrieve, consult, use, consolidate, block, erase or destruct my personal data as part of my information.
</p>

<p class="indent pb-2">
	I hereby affirm my right to be informed, object to processing, access and rectify, suspend, or withdraw my personal data, and be indemnified in case of damages pursuant to the provisions of the Republic Act No. 10173 of the Philippines, Data Privacy Act of 2012 and its corresponding Implementing Rules and Regulations.
</p>
